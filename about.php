		<div><p><br />
	The last season, 2001, saw the Gunners emerge as Grand Finalists, losing
                    narrowly in an exciting and absorbing Grand Final at St. George Stadium to
                    the Division One Premiers, Sydney University. Both Clubs were promoted to
                    the Super League competition in 2002. As well, the revamped traditional
                    home ground of the Gunners, Magdala Park, nestled on a bend of the
                    picturesque Lane Cove River, is now the showpiece surface of the Federation.
                    <br/><br/>
                    <span class="small italic">Article kindly supplied by George Moscos of the Ryde City club
                    - 04/01/2002</span>
                    <br/><br/>
                    Fast forward to 2005 and the club began a revival of its
                    Association side through the support of Tim Thorne, Director of Coaching
                    and Principal of Kickstart. Under Tim’s professional guidance the club put
                    all its young players through an enhanced training program, known as
                    co-operative training. This skills based training presented the game of
                    soccer in a fun and collaborate way, whilst providing the necessary skills
                    to take them further in the years ahead.
                    <br/><br/>
                    In 2007 the club registered 25 Association (GHFA) teams comprising
                    12 Minis, 7 Juniors and 6 Seniors as well as 5 Youth League and one State
                    League team through NSW Soccer (under Ryde City Gunners).
                    <br/><br/>
                    At the conclusion of the 2007 season the club had won 4
                    Premierships (U9/2, AA7, AA20 and O35/2) a Pennant (U11/2) and our AA20
                    and O35/2s backing up to win the Championship as well. All our teams did so
                    well that for the first time in years, North Ryde Soccer Club ranked in the
                    top 10 of the GHFA Club Championship.
                    <br/><br/>
                    During July the club hosted a Texas Hold ‘em Poker night which
                    was well attended by parents and senior players. Prizes ranged from cash
                    to wine and books but bragging rights was probably the main reason for some
                    to participate.
                    <br/><br/>
                    <img class="narrow graphic-container right" src="../images/club/history_magdala_grounds.jpg">
                    Our end of year Presentation Day for all players on Sept 1st was
                    extremely well attended. The jumping castle was very popular and the relaxed
                    surroundings of Magdala Park meant people were happy to stay and mix. All
                    the positive comments received indicate the club is responding well to
                    players needs.
                    <br/><br/>
                    At the AGM on 7 November 2007 the Board decided that without
                    significant financial support it was unable to continue to run a State
                    League side. This meant unfortunately that the Youth League side would
                    be discontinued as well (NSW Soccer rules). Luckily help came in the form
                    of Donald Robertson. Donald approached Gladesville United who run a State
                    League side and were happy to merge into a new club, to be called
                    Gladesville Ryde Magic. It is proposed that this new club will use
                    Magdala Park for its home games under an agreement with North Ryde Soccer
                    Club. This ensures the continuation of representative soccer locally.
                    <br/><br/>
                    North Ryde Soccer Club Inc will instead focus on its local and
                    longstanding players, and in particular its kids. It is also finding that
                    older players, now Dads, are interested in either playing and/or becoming
                    more involved in the club.
                    <br/><br/>
                    The club will be entering the Northern Womens Soccer Association
                    in 2008 for the first time with a girls U12 team. It is expected that more
                    teams will follow as the popularity of women’s soccer grows.
                    <br/><br/>
                    The club currently utilizes two grounds - North Ryde Oval and
                    Magdala Oval, both within view of North Ryde RSL Club.
