                    North Ryde Soccer Football Club was officially formed in December 1953
                    with George Collier being the driving force behind its formation to cater
                    for the needs of a growing young district.
                    <br/><br/>
                    In a meeting at the North Ryde School of Arts on 10 December
                    1953 it was decided that five pounds was to be tendered to Ryde Council
                    for the use of North Ryde Oval for the duration of the Winter Sports
                    season. It was also moved that a smoko be held on 20th February following
                    to obtain funds for the club.
                    <br/><br/>
                    Its chosen colours of brown and gold served the club for a number
                    of seasons, varying slightly to maroon and gold as the years progressed.
                    In 1986, the club affiliated with the North Ryde R.S.L. club, adopting the
                    Blue and Gold of the present day through that union. (This affiliation was
                    amicably terminated in 1991, although the facilities of that club are
                    widely used by all the players and parents, and the Club maintained
                    Associate Sponsorship until 2000).
                    <br/><br/>
                    For twenty years, the club was content to play district soccer,
                    first in the Northern Suburbs Association and by the mid seventies, the
                    Gladesville - Hornsby Football Association.
                    <br/><br/>
                    In 1973, it entered a team in the NSW Federation Division 6 and
                    had progressed over the years to be only one Division from National League.
                    <br/><br/>
                    It has never forgotten its mission - "to develop youth and cater
                    for the needs of its community".
                    <br/><br/>
                    Up to the acceleration in the mid 1990's, outstanding results
                    had been State League Champions in consecutive years (1985-1986), State
                    League Grand Finalists (1992), Women's State League Division One Premiers
                    (1991) and Champion of Champions Finalists (under 12) in 1991.
                    <br/><br/>
                    The 1993 Season saw the Club reach new heights. In an exceptional
                    year, the club won the Division 2 Premiership by eight points, were Club
                    Champions and took out the Grand Final in front of over 2,000 spectators.
                    Wide media coverage was received in the local papers and national magazines.
                    <br/><br/>
                    In addition, during 1993, the North Ryde Soccer Club negotiated
                    a move whereby our State League sides played their Home Games at the T.G.
                    Millner Field (home of the Eastwood Rugby Club). Our Club also used the
                    facilities of T.G. Millner - "the Gunnerdome" - as a base for most of its
                    Youth League, Junior Development, G.H.F.A. and Social activities.
                    <br/><br/>
                    <img class="narrow graphic-container right" src="../images/club/history_team_2.jpg">
                    1994 was equally spectacular - 6th in the First Division
                    competition; victory over Blacktown City Demons and taking the great
                    Marconi 'to the brink' in the $50,000 Waratah Cup Round 6 as well as
                    finalists in the Reserve Grade. - a most satisfying effort in the inaugural
                    year of the club's first of a Five Year plan for success.
                    <br/><br/>
                    1995 saw even more improvement. Initially, a change of name - to
                    the Ryde City "Gunners" - for the Representative sides, meant a greater
                    support base. All three grades made semi-finals (First Grade finished third)
                    and our Under16 Youth League made the Final. The Association with Eastwood
                    was further extended. Our reputation as "Cup Specialists" gave us the
                    opportunity of playing another Quarter Final - this time in Canberra
                    against the Cosmos.
                    <br/><br/>
                    In 1996, the Club gained extensively. Club Champions! Under 18's
                    were crowned Premiers, Under 21's Premiers, Champions, and First Grade
                    beaten Grand Finalists. In G.H.F.A., Premier League was Division Two
                    Undefeated Premiers, Champions, and Youth League continued to improve,
                    progressing on its cumulative points significantly each year. Finally,
                    another excellent Cup sally with another Quarter Final exit - this time
                    taking Super League Champions, the Parramatta Eagles to a penalty shootout.
                    <br/><br/>
                    Receiving support and sponsorship from Continental Tyres, The
                    North Ryde R.S.L. Club, Lotto Sportswear as well as numerous local
                    businesses, saw the 'Gunners' make 1997 even more remarkable! Over 40
                    teams played in the 'Gunners' colours that year; more than twenty-five
                    teams, seventeen years and under - highlighting the Junior Development
                    Policy established at the start of the Five Year Plan in 1994. The State
                    League were again Club Champions, with First Grade comfortably second,
                    Under 21's third and back-to-back Premierships for the Under 18's. Grand
                    Finalists in all three grades and Champions in Under 21 and Under 18's.
                    As well, the Youth League was promoted to Division One, cumulatively
                    finishing second. Grand Final victories by the Under 13's, 14's and 15's,
                    with the Under 16's Premiers and beaten Grand Finalists.
                    <br/><br/>
                    Successful application was made to the New South Wales Soccer
                    Federation to enter the Super League competition at the end of that year.
                    The Presentation was seen as one of the most professional ever put forward
                    and the club rightfully saw itself for the first time in its history as a
                    member of this elite group.
                    <br/><br/>
                    The first year in the premier NSW Federation league, the Super
                    league, 1998, saw the club finish second last. Much of the club's drama
                    was off the field however when after only the second game of the season,
                    T.G. Millner was declared unsuitable and with much expense, Ryde played
                    its remaining home games at Wentworth Park. This lack of stability did not
                    help the club and after only their second year at the top, they were
                    relegated to Division One, although they finished a creditable ninth in
                    the First Grade.
                    <br/><br/>
                    The year 2000 saw the beginning of a revival as ex-player; John
                    McKie took over the reins as Football Manager with the retirement of Les
                    DeCelis. Much needs to be said of Les DeCelis' commitment to Ryde and as
                    a fitting tribute, the Club bestowed upon him the honour of Life Membership
                    for his devotion towards the club. The year 2000 was the watershed year for
                    Ryde and the Gunners finished last in the Division due to the inexperience
                    of the players and the rebuilding program. Many of the younger players were
                    retained however for 2001 by Steve Mayer; and new coach Tony Dunn also
                    brought along some really exciting and talented players, both from Ryde
                    juniors and from other clubs. The average age of Ryde's First Grade squad
                    was 25 and three quarters of them were under 23.
